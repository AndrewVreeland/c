public class Soap {

    String aroma = "";
    double length = 0;
    double height = 0;
    double width = 0;
    boolean isBubbling = false;

    public Soap(String Thearoma, double theLength, double theHeight, double theWidth )
    {
        aroma = Thearoma;
        length = theLength;
        height = theHeight;
        width = theWidth;
        isBubbling = false;
    }

}
